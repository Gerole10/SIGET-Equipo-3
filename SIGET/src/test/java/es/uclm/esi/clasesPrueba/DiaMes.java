package es.uclm.esi.clasesPrueba;

public class DiaMes {
	private int dia;
	private int mes;
	public int getDia() {
		return dia;
	}
	public void setDia(int dia) {
		this.dia = dia;
	}
	public int getMes() {
		return mes;
	}
	public void setMes(int mes) {
		this.mes = mes;
	}
	public DiaMes(int dia, int mes) {
		super();
		this.dia = dia;
		this.mes = mes;
	}
	
}
