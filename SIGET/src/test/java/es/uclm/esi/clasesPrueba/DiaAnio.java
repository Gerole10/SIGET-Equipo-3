package es.uclm.esi.clasesPrueba;

public class DiaAnio {
	private int dia;
	private int ano;
	public int getDia() {
		return dia;
	}
	public void setDia(int dia) {
		this.dia = dia;
	}
	public int getAno() {
		return ano;
	}
	public void setAno(int ano) {
		this.ano = ano;
	}
	public DiaAnio(int dia, int ano) {
		super();
		this.dia = dia;
		this.ano = ano;
	}
	
}
